def get_input(string):
    return [
        i
        for i in string.strip().split('\n')
    ]


def run(problem, arg, expected=None):
    print(problem.__name__, 'result:', problem(arg), 'expected: ' + str(expected) if expected else None)


open_chunk = "([{<"
close_chunk = ")]}>"

open_complement = {
    ")": "(",
    "]": "[",
    "}": "{",
    ">": "<"
}

close_complement = {
    "(": ")",
    "[": "]",
    "{": "}",
    "<": ">",
}


points = {
    ")": 3,
    "]": 57,
    "}": 1197,
    ">": 25137,
}


def is_corrupted(line, check_complete=False):
    stack = []

    for char in line:
        if char in open_chunk:
            stack.append(char)
        if char in close_chunk:
            expected = close_complement[stack[-1]]
            actual = char
            if expected != actual:
                # log(f"expected {expected} got {actual}")
                return True, char
            stack.pop()

    if stack:
        # log("stack was not empty", stack)
        return False, stack

    # if not check_complete:
    #     return False
    return False, None


from collections import defaultdict


def problem_one(vals):
    valid_results = []
    incomplete_results = []
    counts = defaultdict(lambda: 0)

    for line in vals:
        corrupt, val = is_corrupted(line)
        if corrupt:
            counts[val] += 1
        elif val is None:
            valid_results.append(line)
        else:
            incomplete_results.append(line)

    # log(counts)
    return sum(v * points[k] for k, v in counts.items())


def complete_line(line, stack):
    complement = "".join(
        close_complement[char] for char in reversed(stack)
    )

    return line + complement, complement


def problem_two(vals):
    valid_results = []
    incomplete_results = []
    counts = defaultdict(lambda: 0)

    for line in vals:
        corrupt, val = is_corrupted(line)
        if corrupt:
            counts[val] += 1
        elif val is None:
            valid_results.append(line)
        else:
            incomplete_results.append((line, val))

    completed = []
    fragments = []
    for line, stack in incomplete_results:
        completed_line, fragment = complete_line(line, stack)
        completed.append(completed_line)
        fragments.append(fragment)

    scores = []
    point_map = {
        ")": 1,
        "]": 2,
        "}": 3,
        ">": 4,
    }

    for fragment in fragments:
        score = 0
        for char in fragment:
            score = (score * 5) + point_map[char]
        scores.append(score)

    return sorted(scores)[len(scores)//2]


