fn get_map_coordinate(tiles: &Vec<&str>, x: usize, y: usize) -> char {
  let max_x = tiles[0].len();
  let max_y = tiles.len();

  let y_idx = y % max_y;
  let x_idx = x % max_x;

  let result = tiles[y_idx].chars().nth(x_idx).unwrap();
  return result
}

fn get_trees(tiles: &Vec<&str>, x_slope: usize, y_slope: usize) -> usize {
    let mut x = x_slope;
    let mut y = y_slope;
    let mut trees = 0;
    let max_y = tiles.len();

    while y < max_y {
      let result = get_map_coordinate(&tiles, x, y);

      if result == '#' {
        trees += 1;
      }

      x += x_slope;
      y += y_slope;
    }

    return trees;
}

fn check_slopes(tiles: &Vec<&str>, slopes: &Vec<(usize, usize)>) -> usize {
  let mut probability: usize = 1;
  for slope in slopes.iter() {
    let (x, y) = slope;
    probability *= get_trees(tiles, *x, *y);
  }

  return probability;
}


